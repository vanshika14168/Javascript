//AND operator
let x=90
if(x>=80 && x==90){
    console.log("true")
}
else{
    console.log("false")
}


//OR operator
if(x!=90 || x>100){
    console.log("true")
}
else{
    console.log("false")
}


//NOT EQUAL to
if(x!=99){
    console.log("true")
}
else{
    console.log("false")
}